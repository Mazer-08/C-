//sizeof
#include<stdio.h>
#include<math.h>
void main()
{
  printf("The size of int datatype = %d \n",sizeof(int));
  printf("The size of long datatype = %d \n",sizeof(long));  //long int
  printf("The size of float datatype = %d \n",sizeof(float));
  printf("The size of double datatype = %d \n",sizeof(double));
  printf("The size of long double datatype = %d \n",sizeof(long double));
  printf("The size of char datatype = %d \n",sizeof(char));

  printf("\n");
}
